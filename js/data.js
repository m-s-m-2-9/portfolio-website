  if (!window.MSM_DATA) {
    console.warn('[MSM data.js] window.MSM_DATA not found. ' +
      'Make sure admin-control scripts load before data.js in index.html.');
    window.MSM_DATA = {};
  }

  /* Shorthand — used everywhere in this file */
  var D = window.MSM_DATA;

  /* ══════════════════════════════════════════════════════════════════
     UTILITY HELPERS
  ══════════════════════════════════════════════════════════════════ */

  /** Get DOM element by ID. Returns null silently if not found. */
  function el(id) { return document.getElementById(id); }

  /** Get first matching element. Returns null silently. */
  function qs(selector) { return document.querySelector(selector); }

  /** Get all matching elements as array. */
  function qsa(selector) { return Array.from(document.querySelectorAll(selector)); }

  /** Set innerHTML safely — only if element exists. */
  function setHTML(id, html) {
    var element = el(id);
    if (element) element.innerHTML = html;
  }

  /** Set textContent safely — only if element exists. */
  function setText(id, text) {
    var element = el(id);
    if (element) element.textContent = text;
  }

  /**
   * Build a list-item HTML string for lists/projects pages.
   * @param {number} num    - item number (01, 02...)
   * @param {string} title  - item title
   * @param {string} meta   - item meta text
   * @param {string} status - status key (seen/want/completed/etc.)
   * @param {string} label  - badge label text
   */
  function buildListItem(num, title, meta, status, label) {
    var numStr = num < 10 ? '0' + num : '' + num;
    return [
      '<div class="list-item">',
        '<div class="list-item-num">' + numStr + '</div>',
        '<div class="list-item-info">',
          '<div class="list-item-title">' + escapeHTML(title) + '</div>',
          '<div class="list-item-meta">'  + escapeHTML(meta)  + '</div>',
        '</div>',
        '<span class="list-item-status status-' + status + '">' + escapeHTML(label) + '</span>',
      '</div>'
    ].join('');
  }

  /** Escape HTML special characters to prevent XSS. */
  function escapeHTML(str) {
    if (typeof str !== 'string') return '';
    return str
      .replace(/&/g,  '&amp;')
      .replace(/</g,  '&lt;')
      .replace(/>/g,  '&gt;')
      .replace(/"/g,  '&quot;')
      .replace(/'/g,  '&#39;');
  }

  /* ══════════════════════════════════════════════════════════════════
     PASSWORD SYSTEM
     Reads passwords from D.passwords (admin-control/other/passwords.js)
  ══════════════════════════════════════════════════════════════════ */

  /**
   * Unlock a password-gated section.
   * Called by the inline onclick on each unlock button.
   * @param {string} inputId   - ID of the <input type="password"> field
   * @param {string} sectionId - ID of the <div class="secret-content"> to reveal
   * @param {string} pwKey     - key in D.passwords (e.g. 'about', 'photos')
   */
  window.unlockSection = function (inputId, sectionId, pwKey) {
    var inputEl   = el(inputId);
    var sectionEl = el(sectionId);
    if (!inputEl || !sectionEl) return;

    var entered  = inputEl.value;
    var correct  = D.passwords[pwKey] || '';

    if (!correct) {
      /* No password set — just unlock */
      sectionEl.style.display = 'block';
      return;
    }

    if (entered === correct) {
      sectionEl.style.display = 'block';
      inputEl.closest('.inline-password-form') &&
        inputEl.closest('.inline-password-form').remove();
    } else {
      inputEl.value = '';
      inputEl.placeholder = 'Incorrect. Try again.';
      inputEl.style.borderColor = '#f87171';
      setTimeout(function () {
        inputEl.style.borderColor = '';
        inputEl.placeholder = 'password';
      }, 1800);
    }
  };

  /* ── Birthday unlock (special case — uses own key) ─────────────── */
  /* Keeps backward compat with older inline onclick in index.html */
  window.unlockBirthday = function () {
    window.unlockSection('bday-pw', 'bday-secret-info', 'birthday');
    /* Inject birth details after unlock */
    setTimeout(renderBirthdayDetails, 300);
  };

