  /**
   * Open a year panel. Called by onclick on year nodes.
   * Also called by main.js click handler.
   */
  window.MSM = window.MSM || {};
  window.MSM.openYear = function (year) {
    var entry = _journeyYears.find(function (y) { return y.year === year; });
    if (!entry) return;

    /* Update active node */
    qsa('.year-node').forEach(function (n) { n.classList.remove('active'); });
    var node = qs('.year-node[data-year="' + year + '"]');
    if (node) node.classList.add('active');

    /* Populate detail panel */
    setText('detail-year',  entry.year);
    setText('detail-title', entry.title || '');
    var bodyEl = el('detail-body');
    if (bodyEl) {
      if (entry.hasContent && entry.body) {
        var paragraphs = entry.body.split('\n\n');
        bodyEl.innerHTML = paragraphs.map(function (p) {
          return '<p>' + escapeHTML(p.trim()) + '</p>';
        }).join('');
      } else {
        var placeholder = (D.journey || {}).emptyPlaceholder || 'Nothing recorded yet.';
        bodyEl.innerHTML = '<p style="color:var(--text3); font-style:italic;">'
                            + escapeHTML(placeholder) + '</p>';
      }
    }

    /* Show the panel */
    var detail = el('year-detail');
    if (detail) {
      detail.classList.add('visible');
    }
  };

