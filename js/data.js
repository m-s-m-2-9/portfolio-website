  /**
   * Switch between tabs on Lists or Projects pages.
   * Called by inline onclick on .list-tab elements.
   * @param {string} tabId   - panel ID suffix (e.g. 'series', 'completed')
   * @param {HTMLElement} el - the clicked tab element
   */
  window.switchListTab = function (tabId, clickedEl) {
    if (!clickedEl) return;
    var tabsContainer = clickedEl.parentElement;
    var pageSection   = tabsContainer ? tabsContainer.closest('.page') : null;

    /* Deactivate all tabs in this tab group */
    if (tabsContainer) {
      Array.from(tabsContainer.querySelectorAll('.list-tab')).forEach(function (t) {
        t.classList.remove('active');
      });
    }
    clickedEl.classList.add('active');

    /* Deactivate all panels in this page section */
    if (pageSection) {
      Array.from(pageSection.querySelectorAll('.list-panel')).forEach(function (p) {
        p.classList.remove('active');
      });
    }

    /* Activate the target panel */
    var targetPanel = el('list-' + tabId);
    if (targetPanel) targetPanel.classList.add('active');
  };

