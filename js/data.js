  /* Open a belief category — called by onclick */
  window.openBelief = function (categoryId) {
    var cat = _beliefCategories.find(function (c) { return c.id === categoryId; });
    if (!cat) return;

    /* Hide grid, show post view */
    var grid     = el('beliefs-overview');
    var postView = el('belief-post-view');
    if (grid)     grid.style.display = 'none';
    if (postView) {
      postView.classList.add('active');

      /* Set label */
      var labelEl = el('belief-view-label');
      if (labelEl) labelEl.textContent = cat.title || '';

      /* Build posts */
      var postsEl  = el('belief-posts-list');
      var posts    = cat.posts || [];
      if (postsEl) {
        postsEl.innerHTML = posts.length
          ? posts.map(function (post) {
              return [
                '<div class="belief-post">',
                  '<div class="belief-post-date">'  + escapeHTML(post.date  || '') + '</div>',
                  '<div class="belief-post-title">' + escapeHTML(post.title || '') + '</div>',
                  '<div class="belief-post-body">'  + escapeHTML(post.body  || '') + '</div>',
                '</div>'
              ].join('');
            }).join('')
          : '<p style="color:var(--text3);">No posts in this category yet.</p>';
      }
    }
  };

  window.closeBelief = function () {
    var grid     = el('beliefs-overview');
    var postView = el('belief-post-view');
    if (grid)     grid.style.display = '';
    if (postView) postView.classList.remove('active');
  };

